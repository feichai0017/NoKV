func main() {
	outDir := flag.String("out", ".", "directory for generated files")
	flag.Parse()

	groups := make(map[string][]specdsl.OpSpec)
	var files []string
	for _, spec := range specs.All() {
		fileName := generatedFileName(spec)
		if _, ok := groups[fileName]; !ok {
			files = append(files, fileName)
		}
		groups[fileName] = append(groups[fileName], spec)
	}

	for _, fileName := range files {
		source, err := emitFile(groups[fileName])
		if err != nil {
			fmt.Fprintf(os.Stderr, "fsmeta-opgen: %v\n", err)
			os.Exit(1)
		}
		formatted, err := format.Source(source)
		if err != nil {
			fmt.Fprintf(os.Stderr, "fsmeta-opgen: format %s: %v\n%s", fileName, err, source)
			os.Exit(1)
		}
		path := filepath.Join(*outDir, fileName)
		if err := os.WriteFile(path, formatted, 0o644); err != nil {
			fmt.Fprintf(os.Stderr, "fsmeta-opgen: write %s: %v\n", path, err)
			os.Exit(1)
		}
	}

	source, err := emitDispatch(specs.All())
	if err != nil {
		fmt.Fprintf(os.Stderr, "fsmeta-opgen: %v\n", err)
		os.Exit(1)
	}
	formatted, err := format.Source(source)
	if err != nil {
		fmt.Fprintf(os.Stderr, "fsmeta-opgen: format dispatch.program.go: %v\n%s", err, source)
		os.Exit(1)
	}
	path := filepath.Join(*outDir, "dispatch.program.go")
	if err := os.WriteFile(path, formatted, 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "fsmeta-opgen: write %s: %v\n", path, err)
		os.Exit(1)
	}

	source, err = emitReadPrograms(specs.ReadAll())
	if err != nil {
		fmt.Fprintf(os.Stderr, "fsmeta-opgen: %v\n", err)
		os.Exit(1)
	}
	formatted, err = format.Source(source)
	if err != nil {
		fmt.Fprintf(os.Stderr, "fsmeta-opgen: format read.program.go: %v\n%s", err, source)
		os.Exit(1)
	}
	path = filepath.Join(*outDir, "read.program.go")
	if err := os.WriteFile(path, formatted, 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "fsmeta-opgen: write %s: %v\n", path, err)
		os.Exit(1)
	}
}

func generatedFileName(spec specdsl.OpSpec) string {
	if spec.FileName != "" {
		return spec.FileName
	}
	return strings.ToLower(spec.Name) + ".program.go"
}

