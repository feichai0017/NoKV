func emitFile(specs []specdsl.OpSpec) ([]byte, error) {
	if len(specs) == 0 {
		return nil, fmt.Errorf("empty spec file")
	}
	if len(specs) == 1 && specs[0].Emitter == "create" {
		return emitCreate(specs[0])
	}
	for _, spec := range specs {
		if spec.Emitter != "operation" {
			return nil, fmt.Errorf("unsupported op spec %q emitter %q", spec.Name, spec.Emitter)
		}
	}
	return emitOperationFile(specs)
}

func emitDispatch(specs []specdsl.OpSpec) ([]byte, error) {
	var b bytes.Buffer
	writeGeneratedHeader(&b)
	b.WriteString("package compile\n\n")
	b.WriteString("import \"github.com/feichai0017/NoKV/fsmeta\"\n\n")
	b.WriteString("func compileAOTDelta(delta SemanticDelta) (CompiledOp, error) {\n")
	b.WriteString("\tswitch delta.Kind {\n")
	for _, spec := range specs {
		if spec.OperationKind == "" {
			return nil, fmt.Errorf("%s missing OperationKind", spec.Name)
		}
		fmt.Fprintf(&b, "\tcase %s:\n", spec.OperationKind)
		fmt.Fprintf(&b, "\t\treturn %s(delta)\n", compiledOpFuncName(spec))
	}
	b.WriteString("\tdefault:\n")
	b.WriteString("\t\treturn CompiledOp{}, fsmeta.ErrInvalidRequest\n")
	b.WriteString("\t}\n")
	b.WriteString("}\n")
	return b.Bytes(), nil
}

